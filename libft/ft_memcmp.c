#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const unsigned char	*t1 = s1;
	const unsigned char	*t2 = s2;
	int					i;

	i = 0;
	while (n-- > 0)
	{
		if (t1[i] != t2[i])
			return (t1[i] - t2[i]);
		i++;
	}
	return (0);
}
